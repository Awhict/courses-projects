import turtle
turtle.setup(650, 350, 200, 200)
turtle.penup()
turtle.fd(-120)
turtle.pendown()
turtle.pensize(3)
turtle.pencolor("blue")
turtle.circle(50, 360)
turtle.penup()
turtle.fd(120)
turtle.pendown()
turtle.pencolor("black")
turtle.circle(50, 360)
turtle.penup()
turtle.fd(120)
turtle.pendown()
turtle.pencolor("red")
turtle.circle(50, 360)

turtle.done()